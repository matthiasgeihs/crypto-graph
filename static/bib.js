//tbi implement primitive descriptions
primitiveDescriptions.set('OWF', String.raw`
  Let \( f:A \to B \) be a function, \( D \) be a probability distribution over \(A\), \(\tau\in\mathbb{N}\), and \(\epsilon \in [0,1]\). We say \(f\) is \( (\tau, \epsilon) \)-one-way for \(D\) if
  $$ \forall M \in \PTM(\tau): \Pr \begin{bmatrix} x \in  A \land f(x) = y:\\ \begin{bmatrix}f(D) \to y;\\ M(y) \to x;\end{bmatrix} \end{bmatrix} \leq \epsilon \enspace.$$

  Let \(A\) be such that \(\forall \tau \in \NN: |A_{\leq\tau}| < \infty\), where \(A_{\leq\tau} = \{x \in A: |x| \leq \tau\}\).
  We say \( f:A \to B \) is \( (\tau, \epsilon) \)-one-way if \(f\) is \( (\tau, \epsilon) \)-one-way for \( U_{A_{\leq \tau}} \), where \( U_{A_{\leq \tau}} \) is the uniform distribution over \(A_{\leq\tau}\).

  <br><br>

    Let \(\epsilon:\NN \to [0,1]\). We say \(f\) is \(\epsilon\)-one-way for \(D\) if for all \(\tau \in \NN\), \(f\) is \((\tau, \epsilon(\tau))\)-one-way for \(D\).

  <br><br>

    Let \(A\) be such that \(\forall \tau \in \NN: |A_{\leq\tau}| < \infty\).
    Let \(\epsilon:\NN \to [0,1]\). We say \(f\) is \(\epsilon\)-one-way if for all \(\tau \in \NN\), \(f\) is \((\tau, \epsilon(\tau))\)-one-way for \( U_{A_{\leq \tau}} \).

  <br><br>

  We say \( f:A \to B \) is one-way if there exists a negligible function \(\epsilon\) such that for all \(\tau \in \NN\), \(f\) is \((\tau,\epsilon(\tau))\)-one-way.
  `);

function getPrimitiveDescription(primid) {
  return primitiveDescriptions.get(primid);
}